const TRANSLATIONS: Record<PluginLocale, Record<string, string>> = {
	"zh-CN": ZH_CN,
	en: EN,
	de: DE,
	ja: JA,
};

export function normalizeLocale(locale: string | undefined): PluginLocale {
	if (locale === "auto") {
		return normalizeLocale(getSystemLocale());
	}
	const normalized = locale?.trim().toLowerCase();
	if (!normalized) {
		return "en";
	}
	if (normalized === "zh" || normalized === "zh-cn" || normalized.startsWith("zh-")) {
		return "zh-CN";
	}
	if (normalized === "en" || normalized.startsWith("en-")) {
		return "en";
	}
	if (normalized === "de" || normalized.startsWith("de-")) {
		return "de";
	}
	if (normalized === "ja" || normalized.startsWith("ja-")) {
		return "ja";
	}
	return "en";
}

export function getLocale(source?: I18nSource): PluginLocale {
	if (typeof source === "string") {
		return normalizeLocale(source);
	}
	if (source && "settings" in source) {
		return normalizeLocale(source.settings?.pluginLanguage);
	}
	return normalizeLocale(source && "pluginLanguage" in source ? source.pluginLanguage : undefined);
}

export function t(source: I18nSource, key: string, vars: Record<string, string | number> = {}): string {
	const locale = getLocale(source);
	const template = TRANSLATIONS[locale][key] ?? EN[key] ?? ZH_CN[key] ?? key;
	// Replace all placeholders in one pass to avoid issues with placeholder-like text in variable values
	return template.replace(/\{(\w+)\}/g, (match, name: string) => {
		const value = vars[name];
		return value !== undefined ? String(value) : match;
	});
}

export function getLocaleOptions(source?: I18nSource): Record<PluginLanguageSetting, string> {
	const locale = getLocale(source);
	return {
		auto: t(locale, "language.auto"),
		"zh-CN": t(locale, "language.zh-CN"),
		en: t(locale, "language.en"),
		de: t(locale, "language.de"),
		ja: t(locale, "language.ja"),
	};
}

function getSystemLocale(): string | undefined {
	return typeof navigator !== "undefined"
		? navigator.language || navigator.languages?.[0]
		: undefined;
}
